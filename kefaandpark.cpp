void dfs(ll u,ll w)
{
    vis[u]=1;
    if(w==m and cat[u]) return;
    if(tree[u].size()==1 and vis[tree[u][0]]) 
    {
        res++;
        return;
    }
    if(cat[u]) w++;
    else w=0;
    for(ll v:tree[u])
    {
        if(!vis[v]) dfs(v,w);
    }
}
