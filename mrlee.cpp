#include<iostream>
using namespace std;

int t,n,a[51][51],vis[51],res;

void call(int i,int sum,int baki)
{
    if(baki==0)
    {
        sum+=a[i][0];
        res=min(res,sum);
        return;
    }
    for(int ii=0;ii<n;ii++)
    {
        if(!vis[ii])
        {
            vis[ii]=1;
            call(ii,sum+a[i][ii],baki-1);
            vis[ii]=0;
        }
    }
    return;
}
int main()
{
    cin>>t;
    while(t--)
    {
        cin>>n;
        for(int i=0;i<n;i++)
        {
            vis[i]=0;
            for(int j=0;j<n;j++)
            {
                cin>>a[i][j];
            }
        }
        res=99999999;
        // vis[0]=1;
        call(0,0,n);
        cout<<res<<endl;
    }
}


